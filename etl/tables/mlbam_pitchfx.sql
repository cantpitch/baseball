    pfx_x decimal(4,2),
    pfx_y decimal(4,2),
    pfx_z decimal(4,2),
    break_y decimal(3,1),
    break_angle decimal(3,1),
    break_length decimal(3,1),
    strike_zone_top decimal(3,2),
    strike_zone_bottom decimal(3,2),
    pitch_type char(2),
    pitch_name varchar(25),
    pitch_type_confidence decimal(4,3),
    zone smallint,
    nasty smallint,
    spin_dir decimal(6,3),
    spin_rate decimal(7,3),

    pitch_result char(1),
    pitch_result_description varchar(50)
);