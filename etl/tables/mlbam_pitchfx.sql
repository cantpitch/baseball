    away_team char(3) not null,
    home_team char(3) not null,
    away_team_score smallint not null,
    home_team_score smallint not null,
    seq_num smallint not null,
    ab_num smallint not null,
    pitch_num smallint not null,
    inning smallint not null,
    half_inning smallint not null,
    event_num smallint,
    balls smallint not null,
    strikes smallint not null,
    --outs smallint not null,
    event varchar(50),
    event_description text,

    batter_id integer not null,
    batter_hand char(1),
    batter_height smallint,
    pitcher_id integer not null,
    pitcher_throws char(1),

    init_speed decimal(4,1),
    init_pos_x decimal(4,3),
    init_pos_y decimal(5,3),
    init_pos_z decimal(4,3),
